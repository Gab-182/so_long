#include "so_long.h"
/*------------------------------------------------------------------------------------------*/
void	ft_putchar(int c)
{
	write(1, &c, 1);
}
/*------------------------------------------------------------------------------------------*/
void	ft_putstr(char *s)
{
	int		len;
	int		i;

	len = 0;
	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
}
/*------------------------------------------------------------------------------------------*/
void	ft_putnbr(int n)
{
	int		len;

	len = 0;
	if (n <= 2147483647 && n >= -2147483648)
	{
		if (n < 0)
		{
			if (n == -2147483648)
				return (ft_putstr("-2147483648"));
			ft_putchar('-');
			n = n * -1;
		}
		if (n <= 9)
			ft_putchar(n + '0');
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
	}
}